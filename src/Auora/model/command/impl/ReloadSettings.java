package Auora.model.command.impl;

import Auora.GameSettings;
import Auora.model.command.Command;
import Auora.model.player.DonatorRights;
import Auora.model.player.Player;
import Auora.model.player.StaffRights;

/**
 * @author Jonny
 */
public class ReloadSettings extends Command {

    public ReloadSettings(StaffRights staffRights, DonatorRights donatorRights, boolean safeOnly) {
        super(staffRights, donatorRights, safeOnly);
    }

    @Override
    public void execute(Player p, String[] args, StaffRights privilege) {
        GameSettings.load();
        p.sendMessage("You have reloaded the settings.");
    }
}
