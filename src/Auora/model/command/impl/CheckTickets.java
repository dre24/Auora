/**
 * @author Jonny
 */
public class CheckTickets extends Command {

    public CheckTickets(StaffRights staffRights, DonatorRights donatorRights, boolean safeOnly) {
        super(staffRights, donatorRights, safeOnly);
    }

    @Override
    public void execute(Player p, String[] args, StaffRights privilege) {
        Tickets.check(p);
    }
}
