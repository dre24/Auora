    public static void exitWorldMap(Player p) {
        switch (p.getConnection().getDisplayMode()) {
            case 1:
            case 0:
                p.getFrames().sendWindowsPane((short) 548, (byte) 0);
                worldMapToNormal(p);
                break;
            case 2:
            case 3:
                p.getFrames().sendWindowsPane((short) 746, (byte) 0);
                worldMapToFull(p);
                break;
        }
    }

    public static void switchWorldMap(Player p) {
        switch (p.getConnection().getDisplayMode()) {
            case 1:
            case 0:
                normalToWorldMap(p);
                break;
            case 2:
            case 3:
                fullScreenToWorldMap(p);
                break;
        }
        p.getFrames().sendWindowsPane((short) 753, (byte) 1);
    }

    public static void switchWindow(Player p, int mode) {
        if (mode >= 2) {
            p.getFrames().sendWindowsPane((short) 746, (byte) 0);
            normalIFaces(p);
            p.getConnection().setDisplayMode(mode);
        } else {
            p.getFrames().sendWindowsPane((short) 548, (byte) 0);
            fullIFaces(p);
            p.getConnection().setDisplayMode(mode);
        }
        p.getFrames().sendConfig(173, p.getWalk().isRunToggled() ? 0 : 1);
    }
